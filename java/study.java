public class study {
    public static void main(String[] args) {
        //定义一个整型变量age并赋值为10
        int age=10;
        //定义一个byte短整型变量page并赋值为10
        byte page=10;
        //定义一个short整型变量num并赋值为8
        short num=8;
        //定义一个字符型变量并赋值'a'
        char ch='a';
        //定一个字符型变量并赋值'b'
        char cha='b';
        //定义一个boolean变量并赋值为true
        boolean bo=true;
        //定义一个单精度浮点型变量
        float fl=2f;
        //输出上方的结果
        System.out.println(age);
        System.out.println(page);
        System.out.println(num);
        System.out.println(ch);
        System.out.println(bo);
        System.out.println(fl);
        //输出字符类型和int类型的计算结果
        System.out.println(cha+age);
    }
}